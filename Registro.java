    @SuppressWarnings("empty-statement")
    private void bRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRegisterActionPerformed
        String usuario=textUsuario.getText();
        String pass=textPass.getText();
        String pass2=textPass2.getText();
        String apodo=textApodo.getText();
        String ipserv=textIP.getText();
        if (usuario.isEmpty()==true || pass.isEmpty()==true || pass2.isEmpty()==true || apodo.isEmpty()==true){
            JOptionPane.showMessageDialog(this,"Debe rellenar todos los campos","Error",JOptionPane.ERROR_MESSAGE);
        }
        else{
            if (pass.compareTo(pass2)!=0){
                JOptionPane.showMessageDialog(this,"Las contrase√±as no coinciden","Error",JOptionPane.ERROR_MESSAGE);
            }
            else{
                try {
                    Socket socketConexion = new Socket(ipserv, 9090);
                    PrintWriter out = new PrintWriter(socketConexion.getOutputStream(), true);
                    BufferedReader in = new BufferedReader(new InputStreamReader(socketConexion.getInputStream()));

                    NetworkInterface ni=NetworkInterface.getByName("net2");
                    Enumeration<InetAddress> eia=ni.getInetAddresses();
                    InetAddress direccion=eia.nextElement();
                    out.println("Hola");
                    out.println("REGISTRAR "+usuario+" "+pass+" "+apodo+" "+direccion);

                    boolean vl=true;
                    while(vl){
                        String cad=in.readLine();
                        if (cad.length()!=0){
                            System.out.println(cad);
                            vl=false;
                        }
                    }
                    // Se pasa al menu de usuario
                    this.hide();
                    new MenuUsuario(usuario,in,out).setVisible(true);

                    
                } catch (UnknownHostException ex) {
                    Logger.getLogger(Registro.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(Registro.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_bRegisterActionPerformed

